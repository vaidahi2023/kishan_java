public class Switch {

	public static void main(String[] args) {
		
		int a=10,b=20,sum=0,choice=1;
		switch(choice)
		{
			case 1:
					sum=a+b;
					System.out.println("sum="+sum);
					break;
			case 2:
				sum=a-b;
				System.out.println("sum="+sum);
				break;
			
		}
		
	}
}


