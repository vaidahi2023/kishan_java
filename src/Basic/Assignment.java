package Basic;
/*
 * +=,-=,/=,*=,%=
 */
public class Assignment {

	
	public static void main(String[] args) {
		
		
		int a=8;
		//a=a+2
		//a+=2
		System.out.println("a+="+(a+=2));
		System.out.println("a+="+(a-=2));
		System.out.println("a+="+(a*=2));
		System.out.println("a+="+(a/=2));
		System.out.println("a+="+(a%=2));
	}
}
