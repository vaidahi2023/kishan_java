package Basic;

/*
 *  types of loop
 *  1.entry control loop
 *  	--for 
 *  	--while 
 *  2.exit control  loop
 *  	--do while
 *  
 *   for(i=1;i<10;i++/i--)
 *   {
 *   	//
 *   }
 */
public class Loops {

	public static void main(String[] args) {
		
		
		int i;
		for(i=10;i>=1;i--)
		{
			System.out.println(i);
		}
	}
}
